	/// </summary>
	RoundedCornerRectangle,
	/// <summary>
	/// Circle
	/// </summary>
	Circle,
	/// <summary>
	/// Rhombus
	/// </summary>
	Rhombus,
	/// <summary>
	/// Odd shape
	/// </summary>
	OddShape
}
